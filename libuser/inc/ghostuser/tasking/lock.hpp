#ifndef GHOSTLIBRARY_TASKING_LOCK
#define GHOSTLIBRARY_TASKING_LOCK

#include <ghost.h>

/**
 *
 */
class g_lock {
protected:
	uint8_t locked;

public:
	g_lock() :
			locked(false) {
	}

	virtual ~g_lock() {
	}

	virtual void lock() {
		g_atomic_lock(&locked);
	}

	virtual void unlock() {
		locked = false;
	}

	bool isLocked() {
		return locked;
	}

};

#endif
